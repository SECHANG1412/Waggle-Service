from pydantic_settings import BaseSettings
from pydantic import Field
from datetime import timedelta

class Settings(BaseSettings):
    db_user: str = Field(..., alias="DB_USER")
    db_password: str = Field(..., alias="DB_PASSWORD")
    db_host: str = Field("localhost", alias="DB_HOST")
    db_port: str = Field("3306", alias="DB_PORT")
    db_name: str = Field(..., alias="DB_NAME")
    prod: bool = Field(False, alias="PROD")

    secret_key: str = Field(..., alias="SECRET_KEY")
    jwt_algorithm: str = Field("HS256", alias="JWT_ALGORITHM")
    access_token_expire_seconds: int = Field(900, alias="ACCESS_TOKEN_EXPIRE")
    refresh_token_expire_seconds: int = Field(604800, alias="REFRESH_TOKEN_EXPIRE")

    admin_username: str | None = Field(None, alias="ADMIN_USERNAME")
    admin_password: str | None = Field(None, alias="ADMIN_PASSWORD")

    google_client_id: str | None = Field(None, alias="GOOGLE_CLIENT_ID")
    google_client_secret: str | None = Field(None, alias="GOOGLE_CLIENT_SECRET")
    google_redirect_uri: str | None = Field(None, alias="GOOGLE_REDIRECT_URI")

    naver_client_id: str | None = Field(None, alias="NAVER_CLIENT_ID")
    naver_client_secret: str | None = Field(None, alias="NAVER_CLIENT_SECRET")
    naver_redirect_uri: str | None = Field(None, alias="NAVER_CALLBACK_URL")

    kakao_client_id: str | None = Field(None, alias="KAKAO_CLIENT_ID")
    kakao_client_secret: str | None = Field(None, alias="KAKAO_CLIENT_SECRET")
    kakao_redirect_uri: str | None = Field(None, alias="KAKAO_CALLBACK_URL")

    frontend_url: str = Field("http://localhost:3000", alias="FRONTEND_URL")

    class Config:
            env_file = ".env"
            case_sensitive = True
            extra = "allow"
            populate_by_name = True

    @property
    def tmp_db(self) -> str:
          return f"{self.db_user}:{self.db_password}@{self.db_host}:{self.db_port}/{self.db_name}"
    
    @property
    def database_url(self) -> str:
        return f"mysql+asyncmy://{self.tmp_db}"
    
    @property
    def sync_database_url(self) -> str:
        return f"mysql+pymysql://{self.tmp_db}"
    
    @property
    def access_token_expire(self) -> timedelta:
        return timedelta(seconds=self.access_token_expire_seconds)

    @property
    def refresh_token_expire(self) -> timedelta:
        return timedelta(seconds=self.refresh_token_expire_seconds)
    

settings = Settings()
